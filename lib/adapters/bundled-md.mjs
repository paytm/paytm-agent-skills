/**
 * bundled-md adapter (v2 multi-skill).
 *
 * For tools with one global memory / conventions file (Windsurf, Aider).
 * Produces a single markdown that contains:
 *   - Banner
 *   - The routing preamble (global rules + decision tree)
 *   - Each skill's SKILL.md content (frontmatter stripped, section headers added)
 *   - Each skill's references inlined
 *   - Assets inlined as fenced code blocks
 *   - Backends excluded (memory budget)
 */

import { existsSync, mkdirSync, writeFileSync, readFileSync } from "node:fs";
import { dirname } from "node:path";
import { packagePath } from "../manifest.mjs";

function ensureDir(d) { if (!existsSync(d)) mkdirSync(d, { recursive: true }); }

function stripFrontmatter(md) {
  return md.replace(/^---\n[\s\S]*?\n---\n+/, "");
}

export function install({ manifest, target, dir, dryRun = false, fileName }) {
  const target_file = `${dir.replace(/\/$/, "")}/${fileName || manifest.name}.md`;

  const banner = `<!-- generated by paytm-skills@${manifest.version} bundle ${manifest.name}@${manifest.version} for ${target.id} - do not edit by hand -->\n\n`;

  const sections = [];

  // 1. Routing preamble (global rules + decision tree)
  if (manifest.routing_preamble) {
    try {
      sections.push(stripFrontmatter(readFileSync(packagePath(manifest.routing_preamble), "utf8")));
    } catch { /* skip */ }
  }

  // 2. Each skill: entry + references
  for (const skill of manifest.skills || []) {
    sections.push(`\n\n---\n\n# Skill: ${skill.name}\n`);
    if (skill.description) sections.push(`\n_${skill.description}_\n`);
    const entryRaw = readFileSync(packagePath(`${skill.path}/${skill.entry}`), "utf8");
    sections.push(stripFrontmatter(entryRaw));

    for (const ref of skill.references || []) {
      const body = stripFrontmatter(readFileSync(packagePath(ref.path), "utf8"));
      sections.push(`\n\n## Reference: ${ref.path}\n\n${body}`);
    }
  }

  // 3. Assets - fenced code blocks
  for (const a of manifest.assets || []) {
    let body;
    try { body = readFileSync(packagePath(a), "utf8"); }
    catch { continue; }
    const lang = a.endsWith(".html") ? "html"
      : a.endsWith(".env") || a.endsWith(".env.example") ? "bash"
      : a.endsWith(".json") ? "json"
      : a.endsWith(".js") || a.endsWith(".mjs") ? "javascript"
      : "";
    sections.push(`\n\n---\n\n# Asset: ${a}\n\n\`\`\`${lang}\n${body}\n\`\`\``);
  }

  const out = banner + sections.join("");

  if (dryRun) {
    console.log(`  [dry-run] write ${target_file} (${out.length.toLocaleString()} bytes)`);
    return { files: 1 };
  }

  ensureDir(dirname(target_file));
  writeFileSync(target_file, out);
  return { files: 1 };
}
