/**
 * bundled-md adapter: single concatenated markdown file.
 *
 * Used by AI tools that read one global rule / memory / conventions file:
 *   - Windsurf (~/.codeium/windsurf/memories/<name>.md)
 *   - Aider    (~/.config/aider/conventions/<name>.md)
 *
 * Concatenates entry + references with section headers, prepended by a small
 * generated banner identifying the source manifest version.
 */

import { existsSync, mkdirSync, writeFileSync, readFileSync } from "node:fs";
import { dirname } from "node:path";
import { packagePath } from "../manifest.mjs";

function ensureDir(d) {
  if (!existsSync(d)) mkdirSync(d, { recursive: true });
}

function stripFrontmatter(md) {
  // Remove a leading YAML frontmatter block (---\n...\n---) so concatenated
  // sections don't accidentally re-trigger frontmatter parsers downstream.
  return md.replace(/^---\n[\s\S]*?\n---\n+/, "");
}

export function install({ manifest, dir, dryRun = false, fileName }) {
  const outFile = `${dir.replace(/\/$/, "")}.md`.endsWith(".md") && !dir.endsWith(".md")
    ? `${dir}/${fileName || manifest.name}.md`
    : dir;

  // dir convention: callers pass an install_dir that's either:
  //  (a) a directory like ~/.codeium/windsurf/memories  -> we write <name>.md inside
  //  (b) a full filename like ~/.codeium/windsurf/memories/paytm-integration.md
  // For predictability we always treat install_dir as a DIR and write <name>.md.
  const target = `${dir.replace(/\/$/, "")}/${fileName || manifest.name}.md`;

  const banner =
    `<!-- generated by paytm-skills@${manifest.version} from manifest ${manifest.name}@${manifest.version} - do not edit by hand -->\n\n`;

  const sections = [];

  // 1. Entry (SKILL.md)
  const entryRaw = readFileSync(packagePath(manifest.entry), "utf8");
  sections.push(`# ${manifest.name}\n\n${stripFrontmatter(entryRaw)}`);

  // 2. References (markdown knowledge)
  for (const ref of manifest.references || []) {
    const body = stripFrontmatter(readFileSync(packagePath(ref.path), "utf8"));
    sections.push(`\n\n---\n\n# Reference: ${ref.path}\n\n${body}`);
  }

  // 3. Assets (env templates, frontend HTML) - inlined as fenced code blocks
  //    so the model sees example wiring without being confused into thinking
  //    they are markdown to render. Backends are excluded from bundles - too
  //    large for memory-budget targets like Windsurf.
  for (const a of manifest.assets || []) {
    let body;
    try {
      body = readFileSync(packagePath(a), "utf8");
    } catch {
      continue; // skip if asset is binary or missing
    }
    const lang = a.endsWith(".html") ? "html"
      : a.endsWith(".env") || a.endsWith(".env.example") ? "bash"
      : a.endsWith(".json") ? "json"
      : a.endsWith(".js") || a.endsWith(".mjs") ? "javascript"
      : "";
    sections.push(`\n\n---\n\n# Asset: ${a}\n\n\`\`\`${lang}\n${body}\n\`\`\``);
  }

  const out = banner + sections.join("");

  if (dryRun) {
    console.log(`  [dry-run] write ${target} (${out.length.toLocaleString()} bytes)`);
    return { files: 1 };
  }

  ensureDir(dirname(target));
  writeFileSync(target, out);
  return { files: 1 };
}
